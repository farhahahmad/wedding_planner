// This code defines a SupplierModel class with properties representing supplier information

class SupplierModel {
  String? id;
  String? userid;
  String? name;
  String? email;
  String? phone;
  String? ssm;
  String? status;

  SupplierModel({
    required this.id,
    required this.userid,
    required this.name,
    required this.email,
    required this.phone,
    required this.ssm,
    required this.status
  });
}